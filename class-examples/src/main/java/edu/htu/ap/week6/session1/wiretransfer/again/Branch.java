package edu.htu.ap.week6.session1.wiretransfer.again;

public class Branch {
	private int code;
	private String name;

	public Branch() {
	}
	
	public Branch(int code, String name) {
		this.code = code;
		this.name = name;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
