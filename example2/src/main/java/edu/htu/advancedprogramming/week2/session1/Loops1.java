package edu.htu.advancedprogramming.week2.session1;

public class Loops1 {
	public static void main(String[] args) {
		for (int i = 0; i < 10; i++) {
			System.out.println("Hello "+i);
		}
		
		int x=0;
		while(x<10) {
			System.out.println("Hello Again "+x);
			x++;
		}
		
		System.out.println("Done");
	}
}
