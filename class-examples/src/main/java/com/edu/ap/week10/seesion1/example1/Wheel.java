package com.edu.ap.week10.seesion1.example1;

public class Wheel {
	private int width;
	private int height;
	private int rimSize;
	private int year;
	private String brand;

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getRimSize() {
		return rimSize;
	}

	public void setRimSize(int rimSize) {
		this.rimSize = rimSize;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

}
